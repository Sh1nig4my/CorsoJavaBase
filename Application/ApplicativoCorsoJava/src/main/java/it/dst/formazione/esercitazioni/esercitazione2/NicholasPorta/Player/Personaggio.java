package it.dst.formazione.esercitazioni.esercitazione2.NicholasPorta.Player;

public class Personaggio {

    private String nome;
    private int eta;




    public Personaggio() {
        this.eta = eta;
        this.nome = nome;


    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getEta() {
        return eta;
    }

    public void setEta(int eta) {
        this.eta = eta;
    }




}



