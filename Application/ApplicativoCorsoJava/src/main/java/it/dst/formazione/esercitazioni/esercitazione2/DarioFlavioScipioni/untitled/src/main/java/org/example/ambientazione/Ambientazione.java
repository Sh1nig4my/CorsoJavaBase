package org.example.ambientazione;

public class Ambientazione {
    String nome="Stazione Spaziale Epsilon 9";
    int membri= 0;

    public Ambientazione() {
        this.nome = nome;
        this.membri = membri;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getMembri() {
        return membri;
    }

    public void setMembri(int membri) {
        this.membri = membri;
    }
}
